import java.util.Scanner;

public class JavaOperators {

    public static void main(String args[]) {
    
       int x = 5, y = 3;
      
      // pre decrement --x, post decrement x++

       System.out.println("X: " + x--); //

       System.out.println("Y: " + --y); //

       System.out.println("X: " + --x); //

       System.out.println("Y: " + y--); //

       System.out.println("Y: " + --y); //

       System.out.println("X: " + x--); //

       System.out.println("X: " + --x); //

       System.out.println("X: " + --x); //

       System.out.println("X: " + x); //

       System.out.println("Y: " + y); //

    }

}
